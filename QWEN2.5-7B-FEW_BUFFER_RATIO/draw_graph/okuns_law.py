def annotate_min_max(ax, x, y, color, max_prefix="Max", min_prefix="Min", dy_max=6, dy_min=-10):
    """Annotate the max and min points of one line."""
    y = np.array(y)

    idx_max = int(np.argmax(y))
    idx_min = int(np.argmin(y))

    ax.annotate(
        f"{max_prefix}: {y[idx_max]:.2f}",
        xy=(x[idx_max], y[idx_max]),
        xytext=(6, dy_max),
        textcoords="offset points",
        color=color,
        fontsize=9
    )

    ax.annotate(
        f"{min_prefix}: {y[idx_min]:.2f}",
        xy=(x[idx_min], y[idx_min]),
        xytext=(6, dy_min),
        textcoords="offset points",
        color=color,
        fontsize=9
    )

def growth_to_level(growth_rates, base=100.0):
    """
    Convert annual growth-rate series into an index series.
    If growth has length 20, output levels for 21 years.
    """
    levels = [base]
    for g in growth_rates:
        levels.append(levels[-1] * (1 + g / 100.0))
    return levels

def annual_mean(monthly_data):
    """12-month average -> annual series."""
    out = []
    for i in range(0, len(monthly_data), 12):
        chunk = monthly_data[i:i+12]
        if len(chunk) == 12:
            out.append(np.mean(chunk))
    return out

def annual_change(series):
    """Year-to-year change in percentage points for unemployment."""
    return [series[i] - series[i-1] for i in range(1, len(series))]

