def annual_mean(monthly_data):
    """Convert monthly series to annual mean series."""
    out = []
    for i in range(0, len(monthly_data), 12):
        chunk = monthly_data[i:i+12]
        if len(chunk) == 12:
            out.append(np.mean(chunk))
    return out
def annotate_min_max(ax, x, y, color, dy_max=6, dy_min=-10):
    y = np.array(y)
    idx_max = int(np.argmax(y))
    idx_min = int(np.argmin(y))

    ax.annotate(
        f"Max: {y[idx_max]:.2f}",
        xy=(x[idx_max], y[idx_max]),
        xytext=(6, dy_max),
        textcoords="offset points",
        color=color,
        fontsize=9
    )

    ax.annotate(
        f"Min: {y[idx_min]:.2f}",
        xy=(x[idx_min], y[idx_min]),
        xytext=(6, dy_min),
        textcoords="offset points",
        color=color,
        fontsize=9
    )

